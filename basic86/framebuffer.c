static unsigned char line_num;
static unsigned char line_pos;
static unsigned char line_count;

static char alpha_dram[LCD_MAX_LINE][LCD_MAX_LINE_POS];

void init_framebuffer(void)
{
  outb(0xAF, LCD_CMD); /* Endable display */
  outb(0xA1, LCD_CMD); /* Display Normal */
  outb(0xAD, LCD_CMD); /* Cursor */
  
  framebuffer_clear();
}

void framebuffer_setline(char line)
{
  line=line&0x7;
  line=line|0xB0;
  outb(line, LCD_CMD);
}

void framebuffer_setcolumn(char column)
{
  char columnh, columnl;
  column=column&0xff;
  columnh=column&0xf0;
  columnh=column>>4;
  columnl=column&0x0f;
  columnh=columnh|0x10;
  columnl=columnl|0x00;
  
  outb(columnh, LCD_CMD);
  outb(columnl, LCD_CMD);
}

static void dis8x5_helper(char ch)
{
  char i;
  if(ch == 0)
    ch = ' ';
  else if(ch < 0x20 || ch > 0x7e)
    ch = '?';
  
  for(i=0; i<5; i++)
    outb( ascii_font_8x5[ch - 0x20][i], LCD_DAT);
}

void framebuffer_clear(void)
{
  char x, y;
	for(y=0; y<LCD_MAX_LINE; y++) {
    framebuffer_setline(y);
    framebuffer_setcolumn(0);
    for(x=0; x<LCD_MAX_LINE_POS; x++) {
      dis8x5_helper(' ');
    }
 	}
  
  line_num = line_pos = 0;
  line_count = 0;
  memset(alpha_dram, 0, sizeof(alpha_dram));
  
  framebuffer_setline(0);
  framebuffer_setcolumn(0);
}

void framebuffer_dis8x5(char line, char column, char ch)
{
  char i;
  framebuffer_setline(line);
  framebuffer_setcolumn(column);

  dis8x5_helper(ch);
}

static void inc_line_num()
{
  line_pos = 0;
  line_count++;
  
  if(line_num < LCD_MAX_LINE-1) {
    line_num++;
    
    framebuffer_setline(line_num);
    framebuffer_setcolumn(0);
  } else {
    char x, y, t;

    for(y=0; y<LCD_MAX_LINE-1; y++) {
      framebuffer_setline(y);
      framebuffer_setcolumn(0);
      
      for(x=0; x<LCD_MAX_LINE_POS; x++) {
        t = alpha_dram[y][x];
        alpha_dram[y][x] = alpha_dram[y+1][x];
        alpha_dram[y+1][x] = (y == LCD_MAX_LINE-2 ? 0 : t);
        
        dis8x5_helper(alpha_dram[y][x]);
      }
    }
    framebuffer_setline(LCD_MAX_LINE-1);
    framebuffer_setcolumn(0);
    for(x=0; x<LCD_MAX_LINE_POS; x++)
      dis8x5_helper(0);
    
    framebuffer_setcolumn(0);
  }
}

unsigned char get_line_count(void) {
  return line_count;
}

static void inc_line_pos(void)
{
  if(line_pos < LCD_MAX_LINE_POS-1)
    line_pos++;
  else {
    inc_line_num();
    line_pos = 0;
  }
}

void framebuffer_putch(char ch)
{
  if(ch == '\n') {
    inc_line_num();
  } else if(ch == '\b' && line_pos) { /* backspace limitted to a single line */
    line_pos--;
    alpha_dram[line_num][line_pos] = 0;
    framebuffer_setcolumn(line_pos * 5);
    dis8x5_helper(0);
    framebuffer_setcolumn(line_pos * 5);
    
  } else {
    alpha_dram[line_num][line_pos] = ch;
    dis8x5_helper(ch);
    inc_line_pos();
  }
}

void framebuffer_print(const char *buff)
{
  while(*buff) {
    framebuffer_putch(*buff);
    buff++;
  }
}
